import { Injectable } from '@angular/core';
import Axios  from 'axios';
import { AuthService } from 'src/app/auth/auth.service';
import { environment } from 'src/environments/environment';


@Injectable({
  providedIn: 'root'
})
export class CollectionsService {

  constructor(private authService: AuthService) { }

  getCollectionsList(page: number, limit: number, filterString: string, searchString: string) {
    return Axios.get( environment.backend_url + '/products/collections_list?page=' + page + '&limit=' + limit + filterString + searchString, {
      headers: {
        Authorization: this.authService.token
      }
    })
    .catch(error => {
      if (error.response.data.detail == "Session expired, Reopen the application!") {
        this.authService.signout();
      }
    });
  }

  createCollection(data) {
    return Axios.post( environment.backend_url + '/products/collections', data, {
      headers: {
        Authorization: this.authService.token
      }
    })
    .catch(error => {
      if (error.response.data.detail == "Session expired, Reopen the application!") {
        this.authService.signout();
      }
    });
  }

  getCollectionDetail(id) {
    return Axios.get( environment.backend_url + '/products/collections/' + id, {
      headers: {
        Authorization: this.authService.token
      }
    })
    .catch(error => {
      if (error.response.data.detail == "Session expired, Reopen the application!") {
        this.authService.signout();
      }
    });
  }

  deleteCollection(id) {
    return Axios.delete( environment.backend_url + '/products/collections?ids=' + id, {
      headers: {
        Authorization: this.authService.token
      }
    })
    .catch(error => {
      if (error.response.data.detail == "Session expired, Reopen the application!") {
        this.authService.signout();
      }
    });
  }

  updateCollectionDetail(data) {
    return Axios.put( environment.backend_url + '/products/collections', data, {
      headers: {
        Authorization: this.authService.token
      }
    })
    .catch(error => {
      if (error.response.data.detail == "Session expired, Reopen the application!") {
        this.authService.signout();
      }
    });
  }

  change_status(ids: number[], status: string, value: boolean) {
    return Axios.put( environment.backend_url + '/products/collections_status_change', {
      ids,
      status,
      value
    }, {
      headers: {
        Authorization: this.authService.token
      }
    })
    .catch(error => {
      if (error.response.data.detail == "Session expired, Reopen the application!") {
        this.authService.signout();
      }
    });
  }

  getCategoriesList() {
    return Axios.get( environment.backend_url + '/products/categories', {
      headers: {
        Authorization: this.authService.token
      }
    })
    .catch(error => {
      if (error.response.data.detail == "Session expired, Reopen the application!") {
        this.authService.signout();
      }
    });
  }
}
